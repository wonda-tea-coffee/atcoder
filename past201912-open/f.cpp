bool asc(const string &s, const string &t) {
  ll slen = s.size(), tlen = t.size();

  for (ll si = 0, ti = 0; si < slen && ti < tlen; si++, ti++) {
    char S = s[si], T = t[ti];
    if (S < 97) S += 32;
    if (T < 97) T += 32;
    if (S < T) return true;
    else if (S > T) return false;
  }
  return slen < tlen;
}

void solve() {
  string s; cin >> s;
  vector<string> v;

  int i = 0, n = s.size();
  while (i < n) {
    int j = i + 1;
    while (j < n && 'a' <= s[j] && s[j] <= 'z') j++;
    // debug3(i, j, s.substr(i, j-i+1));
    v.emplace_back(s.substr(i, j-i+1));
    i = j+1;
  }

  sort(all(v), asc);
  for (string vi : v) cout << vi;
  cout << endl;
}

