const ll MOD = 1000000007; // 10^9 + 7
const int dx[8] = {1, 0, -1, 0, 1, -1, -1, 1};
const int dy[8] = {0, 1, 0, -1, 1, 1, -1, -1};

ll n;
vector<ll> b;
ll memo[21] = {};

ll dfs(int m) {
  if (memo[m] > 0) return memo[m];

  ll maxs = -1, mins = __INT_MAX__, cnt = 0;

  for (int i = 1; i <= n; i++) {
    if (i == m) continue;
    if (b[i] == m) {
      ll s = dfs(i);
      maxs = max(maxs, s);
      mins = min(mins, s);
      cnt++;
    }
  }

  if (cnt == 0) memo[m] = 1;
  else if (cnt == 1) memo[m] = maxs * 2 + 1;
  else memo[m] = maxs + mins + 1;

  return memo[m];
}

void solve() {
  cin >> n;
  b.resize(n + 1);
  for (int i = 2; i <= n; i++)
    cin >> b[i];
  
  memset(memo, 0, sizeof(memo));
  
  outl(dfs(1));
}

