void solve() {
  string s; cin >> s;
  ll ans = 0, len = 0;
  for (int i = 1; i < s.size();) {
    if (s[i-1]=='2' && s[i]=='5') {
      // debug2(i, len);
      len++;
      i += 2;
    } else {
      // debug2(i, len);
      ans += len * (len + 1) / 2;
      len = 0;
      i++;
    }
  }
  debug2(ans, len);
  outl(ans + len * (len + 1) / 2);
}

