ll dist2(ll x1, ll y1, ll x2, ll y2) {
  ll v1 = abs(x1 - x2);
  ll v2 = abs(y1 - y2);
  return v1 * v1 + v2 * v2;
}

void solve() {
  int x1, y1, r; cin >> x1 >> y1 >> r;
  int x2, y2, x3, y3; cin >> x2 >> y2 >> x3 >> y3;

  if (x2 <= x1 - r && x1 + r <= x3 && y1 + r <= y3 && y2 <= y1 - r) NO();
  else YES();

  r *= r;
  if (dist2(x1, y1, x2, y2) <= r &&
      dist2(x1, y1, x2, y3) <= r &&
      dist2(x1, y1, x3, y2) <= r &&
      dist2(x1, y1, x3, y3) <= r) NO();
  else YES();
}

