const ll MOD = 1000000007; // 10^9 + 7

int zeller(int y, int m, int d) {
  if (m == 1) {
    m = 13;
    y--;
  } else if (m == 2) {
    m = 14;
    y--;
  }

  return 365 * y + y / 4 - y / 100 + y / 400 + 306 * (m + 1) / 10 + d - 429;
}

void solve() {
  int y, m, d; cin >> y >> m >> d;
  outl(735369 - zeller(y, m, d));
}

