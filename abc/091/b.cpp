const ll MOD = 1000000007; // 10^9 + 7

void solve() {
  map<string, int> mp;

  int n; cin >> n;
  rep(i, n) {
    string s; cin >> s;
    if (mp.find(s) == mp.end()) {
      mp.insert(make_pair(s, 1));
    } else {
      mp[s]++;
    }
  }

  int m; cin >> m;
  rep(i, m) {
    string s; cin >> s;
    if (mp.find(s) == mp.end()) {
      mp.insert(make_pair(s, -1));
    } else {
      mp[s]--;
    }
  }

  int ans = 0;
  for (auto it = mp.begin(); it != mp.end(); it++) {
    // debug2(it->first, it->second);
    ans = max(ans, it->second);
  }
  outl(ans);
}

