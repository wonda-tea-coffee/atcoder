string repeat(char c, int t) {
  string ret = "";
  for (int i = 0; i < t; i++) ret += c;
  return ret;
}

void solve() {
  int n; cin >> n;
  int cnts[50][26] = {};
  rep(i, n) {
    string s; cin >> s;
    for (int j = 0; j < s.size(); j++)
      cnts[i][s[j] - 'a']++;
  }
  string ans = "";
  for (int j = 0; j < 26; j++) {
    int cnt = __INT_MAX__;
    for (int i = 0; i < n; i++) {
      // debug3(j, i, cnts[i][j]);
      if (cnts[i][j] == 0) {
        cnt = 0;
        break;
      } else {
        cnt = min(cnt, cnts[i][j]);
      }
    }
    // debug2(j, cnt);
    ans += repeat((char)('a' + j), cnt);
  }
  outl(ans);
}

