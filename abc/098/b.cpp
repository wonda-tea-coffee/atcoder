int count(string s1, string s2) {
  int ret = 0;
  for (char c = 'a'; c <= 'z'; c++) {
    if (s1.find(ctos(c)) != string::npos && s2.find(ctos(c)) != string::npos) ret++;
  }
  return ret;
}

void solve() {
  int n; cin >> n;
  string s; cin >> s;

  int ans = 0;
  for (int i = 1; i < n; i++) {
    string s1 = s.substr(0, i);
    string s2 = s.substr(i, n - i);
    // debug2(s1, s2);
    ans = max(ans, count(s1, s2));
  }
  outl(ans);
}

